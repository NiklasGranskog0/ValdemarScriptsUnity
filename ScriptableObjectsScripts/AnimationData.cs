        private int[] GetAnimationArray<T>(T animationEnum) where T : Enum
        {
            return animationEnum switch
            {
                PlayerAnim.Animations => m_PlayerAnimations,
                Enemy.Animations => m_EnemyAnimations,
                PlayerTjelvarAnimations.Animations => m_PlayerTjelvar,
                TjelvarAllAnimationsv2Animations.Animations => m_TjelvarAllAnimationsv2,
                _ => null
            };
        }

        public int EnumToStateHashName<T>(T animationEnum) where T : Enum
        {
            var animationArray = GetAnimationArray(animationEnum);
            var index = (int)(object)animationEnum;

            return animationArray[index];
        }

